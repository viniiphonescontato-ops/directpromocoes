create or replace view public.diaristas_masked
with (security_barrier = true)
as
select
  id,
  name,
  phone,
  case
    when cpf is null then null
    else '***.***.***-' || right(regexp_replace(cpf, '[^0-9]', '', 'g'), 2)
  end as cpf,
  neighborhood,
  sectors,
  status,
  created_at
from public.diaristas;

grant select on public.diaristas_masked to authenticated;

do $$
declare
  audited_table text;
begin
  foreach audited_table in array array[
    'diaristas', 'lojas_redes', 'demandas', 'setores',
    'diarias_setor', 'diarias_empresas', 'registros_financeiros', 'profiles'
  ] loop
    execute format('drop trigger if exists audit_changes on public.%I', audited_table);
    execute format(
      'create trigger audit_changes after insert or update or delete on public.%I for each row execute function public.write_audit_log()',
      audited_table
    );
  end loop;
end;
$$;

