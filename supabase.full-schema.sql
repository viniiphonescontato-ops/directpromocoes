create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, role)
  values (new.id, new.email, 'operador')
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists create_app_profile on auth.users;
create trigger create_app_profile
after insert on auth.users
for each row execute function public.handle_new_auth_user();

create or replace function public.log_app_access(event_name text, client_user_agent text default null)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() is null or event_name not in ('login', 'logout') then
    raise exception 'Evento de acesso invalido';
  end if;
  insert into public.access_logs (user_id, event, user_agent)
  values (auth.uid(), event_name, left(client_user_agent, 500));
end;
$$;

create or replace function public.write_audit_log()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.audit_logs (user_id, table_name, record_id, action, old_data, new_data)
  values (
    auth.uid(),
    tg_table_name,
    case when tg_op = 'DELETE' then old.id::text else new.id::text end,
    tg_op,
    case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) end,
    case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) end
  );
  return coalesce(new, old);
end;
$$;

create or replace function public.protect_diarist_cpf()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if tg_op = 'INSERT' then
    if not public.is_valid_cpf(new.cpf) then raise exception 'CPF invalido'; end if;
    if not public.is_valid_phone(new.phone) then raise exception 'Telefone invalido'; end if;
  else
    if old.cpf is distinct from new.cpf and not public.is_valid_cpf(new.cpf) then
      raise exception 'CPF invalido';
    end if;
    if old.phone is distinct from new.phone and not public.is_valid_phone(new.phone) then
      raise exception 'Telefone invalido';
    end if;
    if old.cpf is distinct from new.cpf and not public.can_view_full_cpf() then
      raise exception 'Sem permissao para alterar CPF';
    end if;
  end if;
  return new;
end;
$$;

create or replace function public.upsert_diarista(payload jsonb)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  existing_cpf text;
  supplied_cpf text := nullif(payload ->> 'cpf', '');
begin
  if auth.uid() is null then raise exception 'Autenticacao obrigatoria'; end if;
  if supplied_cpf like '%*%' then supplied_cpf := null; end if;
  select cpf into existing_cpf from public.diaristas where id = payload ->> 'id';

  if existing_cpf is not null and supplied_cpf is not null
     and existing_cpf is distinct from supplied_cpf and not public.can_view_full_cpf() then
    raise exception 'Sem permissao para alterar CPF';
  end if;

  insert into public.diaristas (id, name, phone, cpf, neighborhood, sectors, status)
  values (
    payload ->> 'id', payload ->> 'name', payload ->> 'phone',
    coalesce(supplied_cpf, existing_cpf), payload ->> 'neighborhood',
    payload ->> 'sectors', coalesce(payload ->> 'status', 'Ativa')
  )
  on conflict (id) do update set
    name = excluded.name,
    phone = excluded.phone,
    cpf = coalesce(supplied_cpf, public.diaristas.cpf),
    neighborhood = excluded.neighborhood,
    sectors = excluded.sectors,
    status = excluded.status;
end;
$$;

